#include<iostream>
using namespace std;
class Area{
	public:
		int area(int l,int b)
		{
			cout<<l*b<<"rect\n";
		}
		int area(int r)
		{
			const float pi=3.14;
			cout<<pi*(r*r)<<"circle\n";
		}
		int area(int l,int b,int h)
		{
			cout<<l*b*h<<"cube\n";
		}
};
int main()
{
	Area a;
	a.area(5);
	a.area(4,6);
	a.area(5,8,3);

}
